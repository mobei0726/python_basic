# 使用\输出'或者"
print('可以通过\'包裹想要输出的字符串')
print("可以通过\"包裹想要输出的字符串")

# 使用\n表示换行
print('学生信息表:\n姓名\n性别\n年龄')

# 使用\\输出\
print('D:\\software')

# 使用\b删除前一个字符
print('abc\be')

# 使用\r让光标回到本行开头，覆盖输出
print('%abc\rdef')

# 使用\t表示制表符，让光标跳转到下一个制表符,一个制表符是四位
print('12341234123412341234')
print('ab\tc')
print('abc\td')
print('abcd\te')


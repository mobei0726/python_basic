# 使用str()，将其他数据类型转换为字符串类型
result1 = str(18)
result2 = str('18.53')
print(type(result1),result1)
print(type(result2),result2)

# 使用float()，将其他数据类型转换为浮点类型
result3 = float(18)
result4 = float('18.53')
print(type(result3),result3)
print(type(result4),result4)

#使用int(),将数据类型转换为整型
result5 = int(18.53)
print(type(result5),result5)
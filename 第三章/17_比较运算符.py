# 使用比较运算符比较数据，相同是True,不相同是Flase
# 数据类型要保持一致

# 判断是否相等用==
# a = 5
# b = 5
# print(a == b)

# 判断是否不相等用!=
# a= 5
# b = 6
# print(a != b)

# 判断大于用>
# a = 5
# b = 6
# print(a > b)

# 判断小于用<
# a = 5
# b = 6
# print(a < b)

# 判断大于等于用>=
# a = 7
# b = 6
# print(a >= b)

# 判断小于等于用<=
# a = 5
# b = 6
# print(a <= b)

# 以上比较运算符对于字符串也适用
# a = 'abc'
# b = 'abc'
# print(a == b)


# 赋值：把数据赋值给变量
a = 10
print(a)

# 加法复合赋值运算符
b = 10
b += 1 #等价于b = b + 1
print(b)

# 减法复合赋值运算符
c = 10
c -= 1 #等价于c = c - 1
print(c)

# 乘法复合赋值运算符
d = 10
d *= 2 #等价于d = d * 2
print(d)

# 除法复合赋值运算符
e = 10
e /= 2#等价于e = e / 2
print(e)

# 取整复合赋值运算符
f = 10
f //= 3#等价于f = f // 2
print(f)

# 取余复合赋值运算符
g = 10
g %= 3  # 等价于 g= g % 3
print(g)

# 指数复合赋值运算符
h = 2
h **= 3#等价于h = h ** 3
print(h)
